#include<iostream>
using namespace std;

/**
 * Left shift (<<)
 * Right shift (>>)
*/

int main () {
    cout << "Left Shift (* 2):"<<endl;
    cout << "(2<<1):" << (2 << 1) << endl;
    cout << "(2<<2):" << (2 << 2) << endl;
    cout << "(15<<2):" << (15 << 2) << endl;

    cout << "Right Shift (/2):"<< endl;
    cout << "(24 >> 1):" << (24 >> 1) << endl;
    cout << "(24 >> 2):" << (24 >> 2) << endl;
    cout << "(21 >> 2):" << (21 >> 2) << endl;
}